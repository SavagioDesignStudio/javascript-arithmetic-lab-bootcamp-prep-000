function add(a, b) {
  return a + b
}

function subtract(a, b) {
  return a - b
}

function multiply(a, b) {
  return a * b
}

function divide(a, b) {
  return a / b
}

function inc(n) {
  var number = n
  number++
  return number
}

function dec(n) {
  var number = n
  number--
  return number
}

function makeInt(n) {
  parseInt('n', 10)
  return n
}

function preserveDecimal(n) {
  parseFloat('n', 10)
  return n
}
